function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function increment(a) {
    return a + 1
}

function decrement(a) {
    return a - 1
}

function makeInt(a) {
    let number = parseInt(a, 10);
    return number;
}

function preserveDecimal(a) {
    let number = parseFloat(a, 10);
    return number;
}